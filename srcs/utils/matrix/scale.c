#include "utils/matrix.h"

t_matrix    scale(float k)
{
    t_matrix ret;

    ret.x[0] = k;
    ret.x[1] = 0;
    ret.x[2] = 0;
    ret.y[0] = 0;
    ret.y[1] = k;
    ret.y[2] = 0;
    ret.z[0] = 0;
    ret.z[1] = 0;
    ret.z[2] = 1;
    return ret;
}